class NaturalRev
{
    public static void main(String arg[])
    {
        System.out.println("Natural no in correct order");
        for(int i=1;i<=10;i++)
        {
            System.out.println(i);
        }
        System.out.println("Natural no in reverse order");
        for(int i=10;i>0;i--)
        {
            System.out.println(i);
        }
    }
}